def headrow(cols):
    global row
    for j, n in enumerate(cols, 1):
        cc = ws.cell(row=row, column=j, value=n)
        cc.fill = HEAD
        cc.font = WHITE_S
        cc.alignment = WRAP
        cc.border = BORDER
    ws.row_dimensions[row].height = 32
    row += 1


def datarow(vals, status_col=None, h=52):
    global row
    for j, v in enumerate(vals, 1):
        out = f"{v}%" if (status_col and j == status_col and isinstance(v, int)) else v
        cc = ws.cell(row=row, column=j, value=out)
        cc.alignment = WRAP
        cc.border = BORDER
        if status_col and j == status_col and isinstance(v, int):
            cc.fill = fill_for(v)
            cc.alignment = CENTER
    ws.row_dimensions[row].height = h
    row += 1


